
public class Soma {
	
	public static int MSomaInt(int n1 , int n2) {
		int soma = n1 + n2;
		return soma;		
	}
	public static float MSomaFloat(float n1 , float n2) {
		float soma = n1 + n2;
		return soma;
	}
	public static String MSomaConcat(String n1 , String n2) {
		String soma = n1 + n2;
		
		return soma;
	}
}
